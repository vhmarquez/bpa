<h2>Why should I help save the environment?</h2>

<p>Today, there are many issues with the environment. Helping to save the environment can reduce air pollution and toxicity in the environment. Also, a cleaner environment would be a lot healthier to humans and other living organisms such as plants and animals.</p>

