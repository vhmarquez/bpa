<h2>What sorts of things pose a threat to the environment?</h2>

<p>Things such as indisposed trash, toxic wastes and chemicals, smoke caused by vehicles (air pollution), smog, and unused electrical equipment are all different types of things that pose a threat to the environment.</p>

<h2>What happens if no action is taken?</h2>

<p>If nothing is done about the environment, the situation will only continue to worsen and produce an unwanted result for society.</p>

