<h2>What is a carbon footprint?</h2>

<p>A carbon footprint is simply a shorthand indicator of how much carbon dioxide is being produced and released by a person or group of people. It is usually expressed as a number in terms of weight, and assumes for a period of time … usually about a year.</p>

<h2>What are fossil fuels?</h2>

<p>Fossil fuels were formed millions of years ago from the decomposing matter of plants and animals. Over an extremely long time, layers of this decayed matter were transformed through heat and pressure into natural gas, oil and coal – fossil fuels. Burning fossil fuels creates much needed energy, but there is a finite amount, and fossil fuel is not renewable. When it’s gone, it’s gone.</p>

<h2>What is global warming?</h2>

<p>Global warming is exactly what the phrase says—an increase in the average surface temperature of Planet Earth. Scientists tell us that the present increase in surface temperature is occurring at a rate faster than ever before in the planet’s history. Further, this increase is directly linked to increased carbon dioxide emissions caused by human activity. The increase in temperature is already affecting the earth’s climate.</p>

<h2>What are the consequences of Global warming?</h2>

<p>Many of the consequences of Global warming are being seen and experienced now. They include: An increase in drought and wildfires; more intense rainstorms; more powerful and dangerous hurricanes; melting glaciers and a rise in sea level; an increase in deadly heat waves and the spread of diseases; and ecosystem shifts and species dying off.</p>

<h2>What is the difference between climate and weather?</h2>

<p>Weather is the term used to describe conditions in the atmosphere at a specific place and time. Weather is measured by things such as temperature, wind, precipitation, humidity, etc., and can change hour-by-hour and day-by-day.</p>

<p>Climate is the weather of a place summarized over a period of time. So even though a place may have rainy weather one day, its average weather over 3 years could be dry, with precipitation well below normal. </p>

